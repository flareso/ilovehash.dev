function tran53(a: number, b: number, c: number, n: number): number {
  return ((TRAN[(a + n) & 0xff] ^ (TRAN[b] * (n + n + 1)) + TRAN[(c & 0xff) ^ TRAN[n]]) & 0xff) >>> 0;
}

function encode32Bytes(out: Uint8Array, options: HashOptions): string {
  // keep behavior consistent with existing algorithms: only hex/base64 supported.
  if (options.encoding === 'base64') return btoa(String.fromCharCode(...out));
  return bytesToHex(out);
}

/**
 * Compute Nilsimsa digest bytes (32 bytes).
 */
export function nilsimsaBytes(input: string | Uint8Array): Uint8Array {
  const data = typeof input === 'string' ? stringToBytes(input) : input;
  const acc = new Uint32Array(256);
  // Sliding window of last 4 bytes (most recent first).
  const w = [0, 0, 0, 0] as number[];
  let wLen = 0;

  for (let offset = 0; offset < data.length; offset++) {
    const c = data[offset]!;

    if (wLen > 1) {
      acc[tran53(c, w[0]!, w[1]!, 0)]++;
    }
    if (wLen > 2) {
      acc[tran53(c, w[0]!, w[2]!, 1)]++;
      acc[tran53(c, w[1]!, w[2]!, 2)]++;
    }
    if (wLen > 3) {
      acc[tran53(c, w[0]!, w[3]!, 3)]++;
      acc[tran53(c, w[1]!, w[3]!, 4)]++;
      acc[tran53(c, w[2]!, w[3]!, 5)]++;
      acc[tran53(w[3]!, w[0]!, c, 6)]++;
      acc[tran53(w[3]!, w[2]!, c, 7)]++;
    }

    // Update window
    if (wLen < 4) wLen++;
    w[3] = w[2]!;
    w[2] = w[1]!;
    w[1] = w[0]!;
    w[0] = c;
  }

  const len = data.length;
  const numTrigrams = len === 3 ? 1 : len === 4 ? 4 : len > 4 ? 8 * len - 28 : 0;
  const threshold = numTrigrams / 256;

  // Digest bits in little-endian bit order, then reverse bytes for canonical output.
  const digest = new Uint8Array(32);
  for (let i = 0; i < 256; i++) {
    if (acc[i]! > threshold) digest[i >> 3] |= 1 << (i & 7);
  }
  // Reverse byte order (to match common Nilsimsa hex output).
  digest.reverse();
  return digest;
}

/**
 * Compute Nilsimsa digest.
 */
export function nilsimsa(input: string | Uint8Array, options: HashOptions = {}): string {
  return encode32Bytes(nilsimsaBytes(input), options);
}

/**
 * Compare two Nilsimsa digests. Result range: -128 (max difference) to 128 (identical).
 *
 * Accepts:
 * - 32-byte digests (`Uint8Array`)
 * - hex-encoded digests (`string`, 64 hex chars)
 */
export function nilsimsaCompare(a: Uint8Array | string, b: Uint8Array | string): number {
  const da = typeof a === 'string' ? hexTo32Bytes(a) : a;
  const db = typeof b === 'string' ? hexTo32Bytes(b) : b;
  if (da.length !== 32 || db.length !== 32) throw new Error('nilsimsaCompare expects 32-byte digests');
  let bits = 0;
  for (let i = 0; i < 32; i++) bits += POPC[(da[i]! ^ db[i]!) & 0xff]!;
  return 128 - bits;
}

function hexTo32Bytes(hex: string): Uint8Array {
  if (hex.length !== 64) throw new Error('Invalid nilsimsa hex length');
  const out = new Uint8Array(32);
  for (let i = 0; i < 32; i++) out[i] = parseInt(hex.slice(i * 2, i * 2 + 2), 16);
  return out;
}

